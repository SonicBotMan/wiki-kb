#!/usr/bin/env python3
"""
memory_to_wiki.py — 从 OpenViking memories 读取提取的知识，回写到 Wiki 页面。

工作流程:
1. 调用 OpenViking API 检索所有 memories（agent + user scope）
2. 过滤掉 wiki 内部操作产生的 noise（dream_cycle / auto_index 相关）
3. 调用 LLM 从 memories 中提取实体、事件、关系
4. 匹配 wiki 已有页面 → append 到 Timeline
5. 新实体 → 创建 draft 页面（status: draft）
6. 更新 graph.json

数据源: OpenViking REST API (替代旧的 state.db)
依赖: GLM-4-flash API, OpenViking endpoint

用法:
  python3 memory_to_wiki.py                  # 处理所有 memories
  python3 memory_to_wiki.py --dry-run        # 仅分析不修改
  python3 memory_to_wiki.py --verbose        # 显示详细输出
"""

